                <h2 id="life-with-braces">LIFE WITH BRACES:</h2>

                <p class="lead">It's important for you to know how to properly take care of your braces throughout your entire orthodontic treatment.</p>

                <h3>Foods to avoid with braces:</h3>
                <ul class="hyphen-list">
                    <li>Chewy foods — bagels, licorice</li>
                    <li>Crunchy foods — popcorn, chips, ice</li>
                    <li>Sticky foods — caramel candies, chewing gum</li>
                    <li>Hard foods — nuts, hard candies</li>
                    <li>Foods that require biting into — corn on the cob, apples, carrots</li>
                </ul>

                <h3 id="soreness-remedies">Soreness Caused from Braces and Appliances</h3>
                <p class="text-justify">When you first get your braces, you may notice that your teeth and mouth feel a little tender or sore. To relieve the pain, we recommend <mark>dissolving one teaspoon of salt in eight ounces of lukewarm water.</mark> Swish and gargle this solution in your mouth for just a couple of minutes (do not swallow the saltwater).</p>

                <p class="text-justify">If the pain is more severe and does not go away after rinsing, you can also <mark>try taking a pain reliever.</mark> It is also not uncommon for your lips, cheeks, and tongue to become irritated for one to two weeks as they toughen and become used to the braces. We would be <mark>happy to give you some wax that you can put over the braces to lessen the tenderness.</mark> If you need some wax, please let us know.</p>

                <h3 id="loose-wires">Loose Wires and Bands</h3>
                <p class="text-justify">The wires and bands on your braces may come loose. If this happens, please contact us as soon as possible so that we can check and repair your appliance. If any piece of your appliance comes off, be sure to save it and bring it to the office with you.</p>

                <p class="text-justify">If the loose wire is causing irritation to your lips or cheeks, put wax or a wet cotton ball over the broken wire to relieve the pain.</p>

                <h3 id="caring-for-appliances">Take Care of Your Appliances</h3>
                <p class="text-justify">Damaged appliances can increase the length of your treatment process, so be sure to take care of all your appliances. Your teeth and jaw can only move into their correct positions if you consistently wear the rubber bands, headgear, retainer, or other appliances prescribed by your doctor.</p>

