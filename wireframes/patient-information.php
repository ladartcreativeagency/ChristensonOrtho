                <p class="text-justify">You're one step closer to achieving the smile you've always wanted! After your consultation, we will schedule your first appointment and your doctor will place your new orthodontic appliance. This appointment generally takes about an hour, and during this time our team will be available to make sure that you are as comfortable as possible.</p>

                <h3>Scheduling Appointments:</h3>

